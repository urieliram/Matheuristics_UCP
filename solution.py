class Solution:
   def __init__(self, u,v,w,p,r):
       self.u=u
       self.v=v
       self.w=w
       self.p=p
       self.r=r
       
   def getU(self):
       return self.u
   def getV(self):
       return self.v
   def getW(self):
       return self.w
   def getP(self):
       return self.p
   def getR(self):
       return self.r 